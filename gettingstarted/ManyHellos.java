package gettingstarted;

public class ManyHellos
{

	public static void main(String[] args)
	{
		System.out.println("USA:\t\"Hello World\""); //backslash changes meaning of character following it
		System.out.println("Mexico:\t\"Hello World " + "Hello USA\"");// + is used for concatenation
		System.out.println("Hello World");
		System.out.println("Hello World");
	}

}
